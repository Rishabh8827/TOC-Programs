#include<iostream>
using namespace std;

int main()
{
    int n0 = 0,n1 = 0;
    int i,strsize;
    bool done0, done1;
    string a;
    string checkO;
    cout<<"\n****************************************************************************************\n";
    cout<<"\nProgram For PDA Which Accpets Strings Of (0^n)(1^n), n >= 1\n";
    cout<<"\n****************************************************************************************\n";
    cout<<"\nEnter A String : ";
    cin>>a;
    cout<<"\n****************************************************************************************\n";
    strsize = a.size();
    for(i = 0; i < strsize; i++) 
    {
        switch(a[i]){
            case '0':{ 
                n0++;
                if(done0 != true){
                    done0 = true;
                    checkO += '0';
                }
                break;
            }
            case '1': {
                n1++;
                if(done1 != true){
                    done1 = true;
                    checkO += '1';
                }
                break;
            }
            default : break;
        }
    }
    if(n0 == n1 && n0 != 0 && n1 != 0 && checkO == "01")
    {
        cout<<"\n****************************************************************************************\n";
        cout<<"\nYour String is Accepted.\n";
        cout<<"\n****************************************************************************************\n";
    }
    else
    {
        cout<<"\n****************************************************************************************\n";
        cout<<"\nYour String is Rejected.\n";
        cout<<"\n****************************************************************************************\n";
    }    
    return 0;
}
